# understands how to add and subtract numbers
class Calculator

  def add(number_1, number_2)
    number_1 + number_2
  end

  def subtract(number, number_to_subtract)
    number - number_to_subtract
  end
end

# understand how to print the output of whatever is passed to it
class Printer
  def print(output)
    "Output: #{output}"
  end
end
